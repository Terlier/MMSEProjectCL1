package main.java.Model;

public class ClientNeeds {
    private float decorations;
    private float filmingAndPhotos;
    private float artWork;
    private float music;
    private float foodAndDrinks;
    private float computerRelatedIssues;

    public float getDecorations() {
        return decorations;
    }

    public void setDecorations(float decorations) {
        this.decorations = decorations;
    }

    public float getFilmingAndPhotos() {
        return filmingAndPhotos;
    }

    public void setFilmingAndPhotos(float filmingAndPhotos) {
        this.filmingAndPhotos = filmingAndPhotos;
    }

    public float getArtWork() {
        return artWork;
    }

    public void setArtWork(float artWork) {
        this.artWork = artWork;
    }

    public float getMusic() {
        return music;
    }

    public void setMusic(float music) {
        this.music = music;
    }

    public float getFoodAndDrinks() {
        return foodAndDrinks;
    }

    public void setFoodAndDrinks(float foodAndDrinks) {
        this.foodAndDrinks = foodAndDrinks;
    }

    public float getComputerRelatedIssues() {
        return computerRelatedIssues;
    }

    public void setComputerRelatedIssues(float computerRelatedIssues) {
        this.computerRelatedIssues = computerRelatedIssues;
    }
}
